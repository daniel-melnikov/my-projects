--Определение диапазонов заработных плат:
SELECT 
    MIN(salary_from) AS min_salary_from,
    MAX(salary_from) AS max_salary_from,
    AVG(salary_from) AS avg_salary_from,
    MIN(salary_to) AS min_salary_to,
    MAX(salary_to) AS max_salary_to,
    AVG(salary_to) AS avg_salary_to
FROM public.parcing_table;

-- Результат:
-- min_salary_from|max_salary_from|avg_salary_from    |min_salary_to|max_salary_to|avg_salary_to      |
-- ---------------+---------------+-------------------+-------------+-------------+-------------------+
--            50.0|       398000.0|109525.086206896552|      25000.0|     497500.0|153846.714912280702|

--Средняя заработная плата аналитика данных варьируется от 110000 до 154000.
--Верхняя граница минимальной заработной платы составляет 25000, а нижняя 50, что является явной ошибкой в данных.
--Максимальные зарплаты аналитиков данных варьируются от 398000 до 498000


