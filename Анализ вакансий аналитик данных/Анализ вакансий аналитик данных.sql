-- Ключевые навыки на позиции аналитика данных:
SELECT 
    key_skill,
    COUNT(*) AS count
FROM (
    SELECT key_skills_1 AS key_skill FROM public.parcing_table
    UNION ALL
    SELECT key_skills_2 FROM public.parcing_table
    UNION ALL
    SELECT key_skills_3 FROM public.parcing_table
    UNION ALL
    SELECT key_skills_4 FROM public.parcing_table
) AS all_key_skills
GROUP BY key_skill
ORDER BY count DESC
LIMIT 5;

-- Результат:
-- key_skill                          |count|
-- -----------------------------------+-----+
--                                    | 2258|
-- SQL                                |  760|
-- Python                             |  452|
-- Анализ данных                      |  395|

-- Ключевые навыки: SQL, Python, Анализ данных.
-- Также отметим, что в большинстве случаев в вакансиях указано менее 4 ключевых навыков, отсюда и возникают NULL-значения.

-- Soft skills на позиции аналитика данных:
SELECT 
    soft_skill,
    COUNT(*) AS count
FROM (
    SELECT soft_skills_1 AS soft_skill FROM public.parcing_table
    UNION ALL
    SELECT soft_skills_2 FROM public.parcing_table
    UNION ALL
    SELECT soft_skills_3 FROM public.parcing_table
    UNION ALL
    SELECT soft_skills_4 FROM public.parcing_table
) AS all_soft_skills
