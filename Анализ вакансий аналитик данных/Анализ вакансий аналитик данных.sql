--Типы занятости и графики работы аналитиков в активных вакансиях:
SELECT 
    employment,
    schedule,
    COUNT(DISTINCT id) AS id_count
FROM public.parcing_table
GROUP BY employment, schedule 
ORDER BY id_count DESC;

-- Результат:
-- employment         |schedule        |id_count|
-- -------------------+----------------+--------+
-- Полная занятость   |Полный день     |    1425|
-- Полная занятость   |Удаленная работа|     296|
-- Полная занятость   |Гибкий график   |      35|
-- Стажировка         |Полный день     |      10|
-- Полная занятость   |Сменный график  |       8|
-- и т.д.

--За явным преимуществом лидирует полная занятость на полный рабочий день, также популярна полная занятость на удаленном рабочем дне.

